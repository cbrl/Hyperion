#pragma once

#include "component/component.h"


class Name : public ecs::Component<Name> {
public:
	//----------------------------------------------------------------------------------
	// Constructors
	//----------------------------------------------------------------------------------
	Name(std::string name)
		: name(std::move(name)) {
	}

	Name() = default;
	Name(const Name&) = delete;
	Name(Name&&) noexcept = default;

	//----------------------------------------------------------------------------------
	// Destructor
	//----------------------------------------------------------------------------------
	~Name() = default;

	//----------------------------------------------------------------------------------
	// Operators
	//----------------------------------------------------------------------------------
	Name& operator=(const Name&) = delete;
	Name& operator=(Name&&) noexcept = default;

	//----------------------------------------------------------------------------------
	// Member Variables
	//----------------------------------------------------------------------------------
	std::string name;
};